<li><u>異象</u></li>
<br>教會會員得到裝備，參與教會宣教事工，到各國各民中為主得人，建立基督的國度。
<br>&nbsp;</br>

<li><u>目標</u></li>
<br><ul>1. 使教會能跨出本地傳福音的局限，成爲放眼世界，有宣教及國度異象的教會。</ul>
<ul>2. 與教會支持的宣教士，牧師，或其他宣教機構，保持聯繫，以建立短宣或長宣的伙伴關係。</ul>
<ul>3. 培養本教會的宣教人才，鼓勵弟兄姊妹上神學院進修，以待 神的呼召。</ul>


